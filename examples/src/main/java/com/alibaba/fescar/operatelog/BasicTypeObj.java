package com.alibaba.fescar.operatelog;

/**
 * @author: wenyixicodedog
 * @create: 2021-01-31
 * @description: 基本类型对象
 */
public class BasicTypeObj {

    private Integer a;
    private Long b;
    private Float c;
    private Double d;
    private String e;

    public Integer getA() {
        return a;
    }

    public void setA(Integer a) {
        this.a = a;
    }

    public Long getB() {
        return b;
    }

    public void setB(Long b) {
        this.b = b;
    }

    public Float getC() {
        return c;
    }

    public void setC(Float c) {
        this.c = c;
    }

    public Double getD() {
        return d;
    }

    public void setD(Double d) {
        this.d = d;
    }

    public String getE() {
        return e;
    }

    public void setE(String e) {
        this.e = e;
    }

}
